import { MemoryRouter as Router, Routes, Route } from 'react-router-dom';

import routes from './Routes';

export default function AppRoutes() {
  return (
    <Router>
      <Routes>
        <Route path='/'>
          {routes.map(({ id, isDefault, path, component: Component }) => (
            <Route
              index={isDefault}
              key={id}
              path={isDefault ? undefined : path}
              element={<Component />}
            />
          ))}
        </Route>
      </Routes>
    </Router>
  );
}
